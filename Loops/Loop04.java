/*
 Faça um programa que escreve os 100 primeiros quadrados perfeitos (números
 naturais): 0, 1, 4, 9, 16, ...
 */
public class Loop04 {
    public static void main(String args[]) {

        // Variáveis
        int i = 0;

        // Loop e saída de dados
        while (i < 100) {
            System.out.println(i * i);
            i++;
        }
    }
}
